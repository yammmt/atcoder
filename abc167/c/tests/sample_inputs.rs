use cli_test_dir::*;

const BIN: &'static str = "./main";

#[test]
fn sample1() {
    let testdir = TestDir::new(BIN, "");
    let output = testdir
        .cmd()
        .output_with_stdin(r#"3 3 10
60 2 2 4
70 8 7 9
50 2 3 9
"#)
        .tee_output()
        .expect_success();
    assert_eq!(output.stdout_str(), "120\n");
    assert!(output.stderr_str().is_empty());
}

